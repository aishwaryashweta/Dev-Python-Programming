def add(x,y):
    """
    args: x,y 
    this function is used to add two numbers
    """
    return x+y

def mul(x,y):
    """
    args: x,y 
    this function is used to mul two numbers
    """
    return x*y

def sub(x,y):
    return x-y

def div(x,y):
    return x/y

def mod(x,y):
    return x%y